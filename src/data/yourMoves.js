export function getYourMove(chapter) {
  if (!chapter) return null
  if (chapter.yourMove) return chapter.yourMove
  if (yourMoves[chapter.id]) return yourMoves[chapter.id]
  if (chapter.thinkQuestion && typeof chapter.thinkQuestion === 'object') {
    return chapter.thinkQuestion
  }
  return null
}
