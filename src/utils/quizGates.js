export function normalizeGateSecret(value) {
  return String(value ?? '')
    .trim()
    .toLowerCase()
    .replaceAll('ё', 'е')
}

export function getQuizGate(chapterId) {
  return quizGates[Number(chapterId)] ?? null
}

export function checkQuizGate(chapterId, value) {
  const gate = getQuizGate(chapterId)
  if (!gate) return true
  const answer = normalizeGateSecret(value)
  const secret = normalizeGateSecret(gate.secret)
  return answer === secret || answer.includes(secret)
}
